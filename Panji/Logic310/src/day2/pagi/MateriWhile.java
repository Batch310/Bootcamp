package day2.pagi;

public class MateriWhile {

	public static void main(String[] args) {
		// Perulangan Cetak 5 kali

		// WHILE
		int i = 1;
		while (i <= 5) {
			System.out.println("Panji");
			i++; // i - i + 1
		}

		System.out.println();
		// DO-WHILE
		int j = 1;
		do {
			System.out.println("Arya");
			j++;
		} while (j <= 5);
	}

}
