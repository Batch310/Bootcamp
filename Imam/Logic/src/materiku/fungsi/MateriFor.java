package materiku.fungsi;

public class MateriFor {

	public static void main(String[] args) {
		
		//TRANFORMASI BENTUK LOPPING
		
		int i=0;
		while(i<5) {
			System.out.println("Imam");
			i++;	
		}
		
		System.out.println();
		
		//Contoh For, mirip while
		int j = 0;
		for(;j<5;) {
			System.out.println("rika");
			j++;
		}
		
		System.out.println();
		
		//Contoh for yang sebenarnya
		for(int k=0; k<5;k++) {
			System.out.println("aditya");
		}
			
			
		}
		
	}
	
	
	

