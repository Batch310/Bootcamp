package day2.pagi.ArrayDanFor;

public class MateriWhile {

	public static void main(String[] args) {

		//WHILE
		// perulangan cetak nama 5 kali		
		int i=1;
		
		
		while(i<=5) {
			System.out.println("Yuhan");
			i++; // i=i+1
			
		//penjelasan : butuh variabel pembantu untuk menyatakan kondisi, i++ dibutuhkan agar berhenti 
		}
		
		
		do {
			System.out.println("Nama");
			i++;
		} while (i<=5);
		
		
	}

}
