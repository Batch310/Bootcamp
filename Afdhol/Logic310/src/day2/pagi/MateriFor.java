package day2.pagi;

public class MateriFor {

	public static void main(String[] args) {

		//FOR 
		
		//contoh for mirip while
		int j = 1;
		for(;j<=5;) {
			System.out.println("Nanda");
			j++;
		}
		
		//for bentuk asli
		
		System.out.println();
		
		for(int k=1;k<=5;k++) {
			System.out.println("Adi");
		}
	}

}
