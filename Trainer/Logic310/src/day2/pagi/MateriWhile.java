package day2.pagi;

public class MateriWhile {
	public static void main(String[] args) {

		// Perulangan cetak nama kita 5 kali
		int i = 1;

		//Contoh While
		while(i <= 5) {
			System.out.println("Yuhan");
			i++; // i = i + 1
		}
		
		System.out.println();

		int j = 1;
		//Contoh do While
		do {
			System.out.println("Nama");
			j++;
		} while (j <= 5);
		
	}
}
