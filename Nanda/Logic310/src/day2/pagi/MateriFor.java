package day2.pagi;

public class MateriFor {

	public static void main(String[] args) {
		//Contoh While
		int i=1;
		while(i<=5) {
			System.out.println("Yuhan");
			i++;
		}
		
		System.out.println();
		
		//Contoh For mirip While
		int j=1;
		for(;j<=5;) {
			System.out.println("Nanda");
			j++;
		}
		
		System.out.println();
		
		//Contoh For
		for(int k = 1; k<=5;k++) {
			System.out.println("Adi");
		}
	}

}
