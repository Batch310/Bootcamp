package catatan.untuk.FT1;

public class MateriWhile {

	public static void main(String[] args) {

		//Perulangan cetak nama kita 5 kali
		
		int i = 0;
		// contoh while
	while(i < 5) {
			System.out.println("Nanda");
			i++; // i = i+1
		}
		
		//contoh do while
		do {
			System.out.println("Nama");
			i++;
		}while (i<5);
	}
}
