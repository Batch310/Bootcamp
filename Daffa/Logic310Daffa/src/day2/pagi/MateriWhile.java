package day2.pagi;

import java.util.Scanner;

public class MateriWhile {

	public static void main(String[] args) {

		// Perulangan cetak nama 5x
		int i = 0;

		// While
		while (i < 5) {
			System.out.println("James");
			i++; // increment (i = i + 1)
		}

		System.out.println();
		// Do - While

		int j = 0;
		do {
			System.out.println("Bronx");
			j++;
		} while (j < 5);

	}

}