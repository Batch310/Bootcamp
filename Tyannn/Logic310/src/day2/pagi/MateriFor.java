package day2.pagi;

public class MateriFor {

	public static void main(String[] args) {
		// Contoh for, mirip while
		int j = 1;
		for (; j <= 5;) {
			System.out.println("Tyann");
			j++;
		}
		System.out.println();

		// Contoh for, ultimate form
		for (int k = 1; k <= 5; k++) {
			System.out.println("Iyann");
		}
	}
}
